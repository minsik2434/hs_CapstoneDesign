package com.example.project_main;

public class NutritionDto {

    private int kcal;
    private float carbohydrate;
    private float protein;
    private float province;
    private float sugars;
    private float salt;
    private float cholesterol;
    private float saturated_fat;
    private float trans_fat;

    public int getKcal() {
        return kcal;
    }
    public float getCarbohydrate() {
        return carbohydrate;
    }
    public float getProtein() {
        return protein;
    }
    public float getProvince() {
        return province;
    }
    public float getSugars() {
        return sugars;
    }
    public float getSalt() {
        return salt;
    }
    public float getCholesterol() {
        return cholesterol;
    }
    public float getSaturated_fat() {
        return saturated_fat;
    }
    public float getTrans_fat() {
        return trans_fat;
    }

    public void setKcal(int kcal) {
        this.kcal = kcal;
    }
    public void setCarbohydrate(float carbohydrate) {
        this.carbohydrate = carbohydrate;
    }
    public void setProtein(float protein) {
        this.protein = protein;
    }
    public void setProvince(float province) {
        this.province = province;
    }
    public void setSugars(float sugars) {
        this.sugars = sugars;
    }
    public void setSalt(float salt) {
        this.salt = salt;
    }
    public void setCholesterol(float cholesterol) {
        this.cholesterol = cholesterol;
    }
    public void setSaturated_fat(float saturated_fat) {
        this.saturated_fat = saturated_fat;
    }
    public void setTrans_fat(float trans_fat) {
        this.trans_fat = trans_fat;
    }



}
